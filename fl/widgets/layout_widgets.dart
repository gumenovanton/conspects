// Container - takes all available space, can have manualy set sizes, used to add a background, margins etc.

// Center - takes all the space on the screen and center a child, to set a background full screen

// SizedBox - to set gaps, and to set child size

// ConstrainedBox - like a SizedBox but i can set min or max size.

// Column - non scrollable widget, like flex column in html

// Row - non scrollable widget, like flex row in html

// Spacer - used in Column and Row to take all space between items

// Wrap - can be used like a Row or Column, but if elements go out of space, they will be wrapped on Next line. I can set direction and gap between elements.

// Expanded - used to take all available space in the screen for a widget and to limit width of a column, because a column takes width by content width

// ClipRRect -  to clip a child corners, for example to set image border redius.

// AspectRatio - set hight/width proportions to the child, like image and container, and i can cover it.

// Stack - to add titles upon a picture

// Positioned - to place a child in absolute position with bottom, top, left, right or rotation angle params regarding the parent, used in Stack

// CircularAvatar - to place Icons or Images in rounded box

// DecorationImage - to set fit to image, for example cover

// Align - widget to align child content into the parent
